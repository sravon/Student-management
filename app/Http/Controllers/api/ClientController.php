     */
    public function store(Request $request)
    {
        $name = $request->input('key');
        $category = $request->input('stu_id');
        $filepath = $request->file('image')->store('customer');
        $a = DB::table('clients')->insert([
            'key' =>  $name ,
            'stu_id' => $category,
            'image' => $filepath
        ]); 
        if($a)
            return response("Data added successfull", 200);
    }

